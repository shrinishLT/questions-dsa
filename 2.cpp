int n;
int arr[200200];
int dp[200200];


int rec(int lvl){
   if(lvl  == n) return 0;

   if(dp[lvl] != -1)return dp[lvl];

   int ans = 1e9;
    
   if(lvl + 1 + arr[lvl] <= n ){
    ans = min(ans , rec(lvl + 1 + arr[lvl]));
   } 

   ans = min(ans , 1 + rec(lvl+1));
   
  return dp[lvl] = ans;
}

void solve(){
  cin>>n;
  for(int i=0;i<n;i++) cin>>arr[i];
  for(int i=0;i<=n;i++) dp[i] = -1;

  cout << rec(0) << endl;
}




